/**
 *op_add - adding integers
 *@a: Num 1
 *@b: Num 2
 *Return: arithmetic expression result
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 *op_sub - substracting integers
 *@a: Num 1
 *@b: Num 2
 *Return: arithmetic expression result
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 *op_mul - multiplying integers
 *@a: Num 1
 *@b: Num 2
 *Return: arithmetic expression result
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 *op_div - dividing integers
 *@a: Num 1
 *@b: Num 2
 *Return: arithmetic expression result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 *op_mod - module of integers
 *@a: Num 1
 *@b: Num 2
 *Return: arithmetic expression result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
